
public class TipeData {


    public static void main(String[] args) {
        System.out.println("Hello World");

        //Tipe data String
        String nama = ("Muhammad Arief Furqany");
        System.out.println(nama);
        System.out.println("ini adalah tipe data String");

        //Tipe data double
        double ipk = 3.82;
        System.out.println(ipk);
        System.out.println("ini adalah tipe data double");

        //Tipe data integer(Int)
        int nim = 220705043;
        System.out.println(nim);
        System.out.println("Ini adalah tipe data Integer(int)");

        //Tipe data Boolean
        int a = 2;
        if (a == 5) {
            System.out.println("adalah benar");
        } else {
            System.out.println("adalah mitos");
        }
        System.out.println("ini adalah percabangan dengan inputan true");





    }
    
}
