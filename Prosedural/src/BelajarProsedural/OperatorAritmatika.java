package BelajarProsedural;

public class OperatorAritmatika{

    public static void main(String[] args) {
        
        //Java Aritmatika
        // Adalah penugasan pada java, seperti penambahan, pembagian, modulus dllP

        //Variable
      int a = 10;
      int b = 20;
      int c = 30;
      int d = 40;

        //penjumlahan
       int penjumlahan = a + b + c + d;
        System.out.println(penjumlahan);
        System.out.println("ini adalah penjumlahan");


        //pengurangan
        int pengurangan = a - b - c - d;
         System.out.println(pengurangan);
         System.out.println("ini adalah pengurangan");

         //pembagian
         int pembagian = a/b/c/d;
         System.out.println(pembagian);
         System.out.println("ini adalah pembagian");

         //perkalian
         int perkalian = a*b*c*d;
         System.out.println(perkalian);
         System.out.println("ini adalah perkalian");

         //modulus (sisa bagi)
         int modulus = a % b % c %d;
         System.out.println(modulus);
         System.out.println("ini adalah modulus");

    }
    
}